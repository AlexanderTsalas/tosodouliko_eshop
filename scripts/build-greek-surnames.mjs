/**
 * One-off generator: reads "Greek Surnames - Sheet1.csv" and emits
 * src/lib/forms/greek-surnames.ts — a TypeScript module exporting SURNAME_DICT.
 *
 * Surnames don't have variants (no Greek diminutive culture for surnames the
 * way there is for first names), so the CSV is simpler: each row is just a
 * Latin,Greek pair. The output dict mirrors greek-names.ts shape so the
 * runtime can use the same normalize/fuzzy logic.
 *
 * Run:  node scripts/build-greek-surnames.mjs
 */

import { readFileSync, writeFileSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const CSV_PATH = resolve(__dirname, "../docs/data/Greek Surnames - Sheet1.csv");
const OUT_PATH = resolve(__dirname, "../src/lib/forms/greek-surnames.ts");

function normalize(s) {
  return s
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "")
    .toLowerCase()
    .replace(/ς/g, "σ")
    .trim()
    .replace(/\s+/g, " ");
}

const csv = readFileSync(CSV_PATH, "utf-8");
const lines = csv.split("\n").map((l) => l.trim()).filter(Boolean);

// The CSV has no header — first line is already data.
const dict = new Map();
let dups = 0;

for (const line of lines) {
  const [latin, greek] = line.split(",").map((s) => s.trim());
  if (!latin || !greek) continue;

  // Same entry stored under both the Latin-normalized and Greek-normalized key.
  for (const [key, lat, gr] of [
    [normalize(latin), latin, greek],
    [normalize(greek), latin, greek],
  ]) {
    if (dict.has(key)) {
      // Collision: keep the first owner; surname pairs don't typically collide
      // but track if it happens.
      dups++;
      continue;
    }
    dict.set(key, { latin: lat, greek: gr });
  }
}

console.log(`Rows: ${lines.length}`);
console.log(`Unique keys: ${dict.size}`);
console.log(`Collisions (kept first owner): ${dups}`);

const tsLines = [
  "// AUTO-GENERATED by scripts/build-greek-surnames.mjs — do not edit manually.",
  "// Re-run:  node scripts/build-greek-surnames.mjs",
  "//",
  `// Source: Greek Surnames - Sheet1.csv (${lines.length} rows)`,
  `// Keys: ${dict.size}${dups > 0 ? ` (${dups} collisions dropped)` : ""}`,
  "",
  'import type { NameEntry } from "./greek-names";',
  "",
  "export const SURNAME_DICT: ReadonlyMap<string, NameEntry> = new Map([",
];

const sortedKeys = Array.from(dict.keys()).sort();
for (const key of sortedKeys) {
  const entry = dict.get(key);
  const lat = entry.latin.replace(/\\/g, "\\\\").replace(/'/g, "\\'");
  const gr = entry.greek.replace(/\\/g, "\\\\").replace(/'/g, "\\'");
  // Use kind:"canonical" to share the NameEntry shape with first names.
  tsLines.push(`  ['${key}', { kind: 'canonical', latin: '${lat}', greek: '${gr}' }],`);
}

tsLines.push("]);");
tsLines.push("");

writeFileSync(OUT_PATH, tsLines.join("\n"), "utf-8");
console.log(`\nWritten to ${OUT_PATH}`);
